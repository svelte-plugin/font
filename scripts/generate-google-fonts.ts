function seedRegistry(): Record<string, FontMeta> {
	const out: Record<string, FontMeta> = {};
	for (const [family, category] of Object.entries(SEED)) {
		out[family] = {
			category,
			axes: [],
			weights: [400, 700],
			subsets: ['latin'],
			variable: false
		};
	}
	return out;
}

async function fetchRegistry(): Promise<Record<string, FontMeta>> {
	const res = await fetch(META_URL);
	if (!res.ok) throw new Error(`metadata endpoint returned ${res.status}`);
	const raw = await res.text();
	// Strip the anti-hijacking junk prefix before the first '{'.
	const start = raw.indexOf('{');
	if (start < 0) throw new Error('no JSON object found in metadata response');
	const data = JSON.parse(raw.slice(start)) as {
		familyMetadataList: Array<{
			family: string;
			category: string;
			axes?: { tag: string; min: number; max: number; defaultValue?: number }[];
			fonts?: Record<string, unknown>;
			subsets?: string[];
		}>;
	};

	const out: Record<string, FontMeta> = {};
	for (const f of data.familyMetadataList) {
		const axes = (f.axes ?? []).map((a) => ({ tag: a.tag, min: a.min, max: a.max }));
		// Weights come from the `fonts` map keys (e.g. "400", "700", "400i").
		const weights = Array.from(
			new Set(
				Object.keys(f.fonts ?? {})
					.map((k) => parseInt(k, 10))
					.filter((n) => Number.isFinite(n))
			)
		).sort((a, b) => a - b);
		out[f.family] = {
			category: mapCategory(f.category),
			axes,
			weights: weights.length ? weights : [400],
			subsets: f.subsets ?? ['latin'],
			// Variable if a weight axis ("wght") is present.
			variable: axes.some((a) => a.tag === 'wght')
		};
	}
	return out;
}

function emit(registry: Record<string, FontMeta>): string {
	const names = Object.keys(registry).sort((a, b) => a.localeCompare(b));
	const union = names.map((n) => `\t| ${JSON.stringify(n)}`).join('\n');
	const entries = names
		.map((n) => `\t${JSON.stringify(n)}: ${JSON.stringify(registry[n])}`)
		.join(',\n');
	const nameList = names.map((n) => `\t${JSON.stringify(n)}`).join(',\n');

	return `// AUTO-GENERATED by scripts/generate-google-fonts.ts — DO NOT EDIT.
// Regenerate with: node scripts/generate-google-fonts.ts

export type GoogleFontName =
${union};

export interface GoogleFontMeta {
	category: 'sans' | 'serif' | 'mono';
	axes: { tag: string; min: number; max: number }[];
	weights: number[];
	subsets: string[];
	variable: boolean;
}

export const googleFonts: Record<GoogleFontName, GoogleFontMeta> = {
${entries}
};

export const GOOGLE_FONT_NAMES: GoogleFontName[] = [
${nameList}
];
`;
}

async function main(): Promise<void> {
	let registry: Record<string, FontMeta>;
	try {
		registry = await fetchRegistry();
		console.log(
			`[generate-google-fonts] OK: fetched ${Object.keys(registry).length} families from ${META_URL}`
		);
	} catch (err) {
		registry = seedRegistry();
		console.warn(
			`[generate-google-fonts] FETCH FAILED (${(err as Error).message}); using hardcoded seed of ${Object.keys(registry).length} families`
		);
	}
	await mkdir(dirname(OUT_PATH), { recursive: true });
	await writeFile(OUT_PATH, emit(registry), 'utf8');
	console.log(`[generate-google-fonts] wrote ${OUT_PATH}`);
}

main().catch((err) => {
	console.error('[generate-google-fonts] FATAL', err);
	process.exit(1);
});
