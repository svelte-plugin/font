// scripts/generate-google-fonts.ts
//
// Fetches the public Google Fonts metadata endpoint (no API key required),
// strips the anti-JSON-hijacking junk prefix, and writes a fully typed
// registry to src/lib/generated/google-fonts.ts.
//
// Run with:  node scripts/generate-google-fonts.ts
//
// Node 24 strips TS types natively, so this file must avoid enums / namespaces /
// decorators / parameter properties. Use plain JS + erasable type annotations only.

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const META_URL = 'https://fonts.google.com/metadata/fonts';
const OUT_PATH = resolve(
	dirname(fileURLToPath(import.meta.url)),
	'../src/lib/generated/google-fonts.ts'
);

type Category = 'sans' | 'serif' | 'mono';

interface FontMeta {
	category: Category;
	axes: { tag: string; min: number; max: number }[];
	weights: number[];
	subsets: string[];
	variable: boolean;
}

// Google category string -> our normalized category union.
function mapCategory(googleCategory: string): Category {
	switch (googleCategory) {
		case 'Monospace':
			return 'mono';
		case 'Serif':
			return 'serif';
		case 'Sans Serif':
			return 'sans';
		// Display / Handwriting / anything unknown -> sans (default)
		default:
			return 'sans';
	}
}

// ~40 popular families used when the network fetch fails, so the generated
// file always exists and typechecks.
