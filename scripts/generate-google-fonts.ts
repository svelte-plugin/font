	Roboto: 'sans',
	'Open Sans': 'sans',
	Lato: 'sans',
	Montserrat: 'sans',
	Poppins: 'sans',
	'Noto Sans': 'sans',
	Raleway: 'sans',
	'Work Sans': 'sans',
	Nunito: 'sans',
	'Nunito Sans': 'sans',
	'PT Sans': 'sans',
	Rubik: 'sans',
	Mulish: 'sans',
	'DM Sans': 'sans',
	Manrope: 'sans',
	Karla: 'sans',
	'Fira Sans': 'sans',
	Quicksand: 'sans',
	Oswald: 'sans',
	'Source Sans 3': 'sans',
	Ubuntu: 'sans',
	Heebo: 'sans',
	'Roboto Mono': 'mono',
	'JetBrains Mono': 'mono',
	'Fira Code': 'mono',
	'Source Code Pro': 'mono',
	'IBM Plex Mono': 'mono',
	'Space Mono': 'mono',
	Inconsolata: 'mono',
